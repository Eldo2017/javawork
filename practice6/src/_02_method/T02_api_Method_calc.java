package _02_method;

public class T02_api_Method_calc {
	int adding(int a, int b) {
		int res1 = a + b;
		return res1;
	}
	int subtracting(int c, int d) {
		int res2 = c - d;
		return res2;
	}
	
	int multiplying(int e, int f) {
		int res3 = e * f;
		return res3;
	}
	
	double dividing(double g, double h) {
		double res4 = g / h;
		return res4;
	}
	
	int modding(int x, int y) {
		int res5 = x % y;
		return res5;
	}
}
