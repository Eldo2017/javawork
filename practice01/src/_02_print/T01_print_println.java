package _02_print;

public class T01_print_println {

	public static void main(String[] args) {
		// 출력하기
		// println - 출력 후에 줄을 바꾼다
		System.out.println("가면라이더 카리에스");
		// 문자열 : 쌍따옴표("")안에 기재
		System.out.println("내 이름은 윤우진"); // ln = line
		// 숫자 : 아무것도 붙이지 않는다
		System.out.println(3+4);
		System.out.println(6*6);
		
		// print : 출력 후 줄을 바꾸지 않는다
		System.out.print("줄을 바꾸지 않는다. 옆에 한 줄로 표시된다. ");
		System.out.print("예시: Horror! Destroy!");
	}

}
